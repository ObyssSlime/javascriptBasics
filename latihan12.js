// pemilihan / switch statements
let buah='mangga'
switch (buah){
	case 'jeruk':
		console.log('anda pilih buah jeruk');
		break;
	case 'apel':
		console.log('anda pilih buah apel');
		break;
	case 'pisang':
		console.log('anda pilih buah pisang');
		break;	
	case 'mangga':
		console.log('anda pilih buah mangga');
		break;
	default:
		console.log('buah tidak tersedia');
		break;
};
