// use string multiple line with normal strings
console.log("hello \n\
world \n\
yeah");

// use string multiple line with template literals
console.log(`hello
world
yeah`);

let one=1, two=2;
// use string multiple line with normal strings
console.log('this is number '+(one+two));

// use string multiple line with template literals
console.log(`this is number ${one+two}`);

// both have the same effect
